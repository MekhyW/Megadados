DROP DATABASE IF EXISTS parques;
CREATE DATABASE parques;
USE parques;

CREATE TABLE administradora (
	id_adm INT NOT NULL,
    nome VARCHAR(80) NOT NULL,
    PRIMARY KEY (id_adm)
);

CREATE TABLE parque (
	id_parque INT NOT NULL,
    nome VARCHAR(80) NOT NULL,
    id_adm INT NOT NULL,
    PRIMARY KEY (id_parque),
    CONSTRAINT fk_adm FOREIGN KEY (id_adm) REFERENCES administradora (id_adm)
);

CREATE TABLE usuario (
	id_usuario INT NOT NULL,
    nome VARCHAR(80) NOT NULL DEFAULT "Anonimo",
    PRIMARY KEY (id_usuario)
);

CREATE TABLE tipo_evento (
	id_tipo INT NOT NULL,
    descricao VARCHAR(80) NOT NULL,
    PRIMARY KEY (id_tipo)
);

CREATE TABLE evento (
	id_evento INT NOT NULL,
    id_usuario INT NOT NULL,
    id_tipo INT NOT NULL,
    id_parque INT NOT NULL,
    data_hora DATETIME NOT NULL,
    comentario VARCHAR(180),
    PRIMARY KEY (id_evento),
    CONSTRAINT fk_usuario FOREIGN KEY (id_usuario) REFERENCES usuario (id_usuario),
    CONSTRAINT fk_tipo FOREIGN KEY (id_tipo) REFERENCES tipo_evento (id_tipo),
    CONSTRAINT fk_parque FOREIGN KEY (id_parque) REFERENCES parque (id_parque)
);

